def add(first_term, second_term):
    return first_term + second_term


def subtract(first_term, second_term):
    return first_term - second_term


def multiple(first_term, second_term):
    return first_term * second_term


def divide(first_term, second_term):
    return first_term / second_term


print("calling add...")
print("result of addition 1 and 2  is =")
print(add(1,2))

print("\n calling substract....")
print("result of subtraction 4 and 2  is =")
print(subtract(4,2))

print("\n calling multiply....")
print("result of multiply 3500 and 2  is =")
print(multiple(3500,2))

print("\n calling divide....")
print("result of divide 1000 and 80  is =")
print(divide(1000,80))
